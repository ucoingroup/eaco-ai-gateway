const rtlLanguages = ["ar"];

let currentLanguage = localStorage.getItem("eaco-lang") || "en";

/**
 * Get translation by key
 * @param {string} key - Translation key
 * @returns {string} - Translated text
 */
function t(key) {
  const lang = currentLanguage;
  if (translations[lang] && translations[lang][key]) {
    return translations[lang][key];
  }
  // Fallback to English
  if (translations.en && translations.en[key]) {
    return translations.en[key];
  }
  return key;
}

/**
 * Set language and update all data-i18n elements
 * @param {string} lang - Language code
 */
function setLanguage(lang) {
  if (!translations[lang]) {
    console.warn(`Language ${lang} not supported`);
    return;
  }
  currentLanguage = lang;
  localStorage.setItem("eaco-lang", lang);

  // Update all elements with data-i18n attribute
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    el.textContent = t(key);
  });

  // Update placeholder attributes
  document.querySelectorAll("[data-i18n-placeholder]").forEach((el) => {
    const key = el.getAttribute("data-i18n-placeholder");
    el.placeholder = t(key);
  });

  // Handle RTL
  const isRTL = rtlLanguages.includes(lang);
  document.documentElement.dir = isRTL ? "rtl" : "ltr";
  document.documentElement.lang = lang;

  // Update language selector display
  const langDisplay = document.getElementById("lang-display");
  if (langDisplay) {
    langDisplay.textContent = languageNames[lang];
  }

  // Update pricing table if rendered
  renderPricingTable();
}

/**
 * Get current language
 * @returns {string} - Current language code
 */
function getCurrentLanguage() {
  return currentLanguage;
}

/**
 * Get all supported language codes
 * @returns {string[]} - Array of language codes
 */
function getSupportedLanguages() {
  return Object.keys(translations);
}

/**
 * Get language display name
 * @param {string} lang - Language code
 * @returns {string} - Display name
 */
function getLanguageName(lang) {
  return languageNames[lang] || lang;
}

